<h1>Bienvenue sur le Forum du Collège Maisonneuve</h1>

<p>
    Ce forum est un espace conçu pour les étudiants du Collège Maisonneuve afin de favoriser les échanges, l'entraide,
    le partage de ressources et la discussion autour de la vie académique. Il s’agit d’un outil de collaboration et de
    communication
    pour tous les membres de la communauté étudiante.
</p>

<h2>Présentation du forum</h2>

<p>
    Le forum vous permet de publier des messages en lien avec vos cours, vos projets scolaires, vos besoins de
    collaboration,
    ou simplement vos expériences étudiantes. C'est également un espace où vous pouvez poser vos questions, offrir votre
    aide
    ou proposer des idées utiles à la communauté.
</p>

<p>
    L'accès complet au forum, notamment la possibilité de publier des messages et de consulter vos publications
    personnelles,
    nécessite la création d’un compte et une connexion à votre espace utilisateur. Cela vous permettra de :
</p>

<ul>
    <li>Publier vos propres messages</li>
    <li>Consulter l'historique de vos publications</li>
    <li>Accéder à votre profil personnel de manière sécurisée</li>
</ul>

<h2>Accès aux publications</h2>

<p>
    Les visiteurs non connectés peuvent tout de même accéder à l'ensemble des publications du forum en lecture seule.
    Cela permet à tous de rester informés, de s’inspirer et de découvrir les sujets déjà abordés par la communauté
    étudiante.
</p>

<h2>Règles de bonne conduite</h2>

<p>
    Pour préserver un climat sain et respectueux sur le forum, il est essentiel de suivre les règles suivantes :
</p>

<ul>
    <li>Adopter un langage courtois et respectueux en tout temps</li>
    <li>Ne pas utiliser de propos insultants, discriminatoires ou offensants</li>
    <li>Ne pas viser ni humilier directement un autre membre</li>
    <li>Respecter les valeurs et les règlements du Collège Maisonneuve</li>
</ul>

<p>
    Le non-respect de ces règles peut entraîner la suppression de messages ou la suspension de votre compte par l’équipe
    de modération.
</p>

<h2>En résumé</h2>

<p>
    Ce forum est un lieu d’échange pour apprendre ensemble, collaborer, s'entraider et grandir dans un environnement
    positif.
    Votre participation contribue à faire vivre cette communauté. Merci d’y prendre part avec respect et bienveillance.
</p>

<p><strong>Bonne visite et excellente participation sur le forum du Collège Maisonneuve.</strong></p>